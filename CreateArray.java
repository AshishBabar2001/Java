public class CreateArray {
    public static void main(String args[]){
        int[] arr = new int[4];

        arr[0] = 90;
        arr[1] = 98;
        arr[2] = 94;
        arr[3] = 96;

        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
        System.out.println(arr[3]);
    }
}
